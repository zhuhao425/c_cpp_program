/*
交换数
    @*p 第一个数
    @*q 第二个数
*/
void swap(int *p, int *q);

/*
冒泡排序 
    @array 待排序数组 
    @len 数组长度
*/
int bubblesort(int array[], int len);

/*
选择排序
    @array 待排序数组 
    @len 数组长度
*/
int selectionsort(int array[], int len);

/*
快速排序
    @array 待排序数组 
    @len 数组长度
*/
int quicksort(int array[], int len);